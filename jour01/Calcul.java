class Calcul {
	
	public static void main(String[] args) {
		
		// déclare la variable "nombre" initialisée à 7
		int nombre = 7;
		System.out.println(nombre);
		
		// ajoute 3 à "nombre"
		nombre += 3;
		System.out.println(nombre);
		
		// multiplie "nombre" par 5
		nombre *= 5;
		System.out.println(nombre);
		
	}
	
}